    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add U your handling code here:
        String nome, cpf, telefone, curso;
        nome = txtNomeAluno.getText();
        cpf = txtCpfAluno.getText();
        telefone = txtTelefoneAluno.getText();
        curso = txtCursoAluno.getText();

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/VarejoPro","root","Sarti007@");
           PreparedStatement st = (PreparedStatement) conectar.prepareStatement("INSERT INTO aluno VALUES(?,?,?,?)");
            st.setString(1,nome);
            st.setString(2,cpf);
            st.setString(3,telefone);
            st.setString(4, curso);
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso");
            //5- Limpar os campos
            txtNomeAluno.setText("");
            txtCpfAluno.setText("");
            txtTelefoneAluno.setText("");
            txtCursoAluno.setText("");
            txtNomeAluno.requestFocus(); // Posiciona o cursor no campo usuario

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro encontrado, entre em contato com suporte e informe o erro:" + ex.getMessage());
        } catch (java.sql.SQLException ex) {
            String erro = ex.getMessage();
            if(erro.contains("Duplicate entry")){
                JOptionPane.showMessageDialog(null, "Aluno j√° cadastrado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro encontrado, entre em contato com suporte e informe o erro:" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

